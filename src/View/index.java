    private void createLines(){
        lineNumbers = new JTextArea("1");
        
        hl = (DefaultHighlighter) taBase.getHighlighter();
        taBase.setHighlighter(hl);
        hl.setDrawsLayeredHighlights(false);
        
        lineNumbers.setBackground(inpar);
        lineNumbers.setEditable(false);
        taBase.getDocument().addDocumentListener(new DocumentListener(){
            @Override
            public void insertUpdate(DocumentEvent e) {
                lineNumbers.setText(getText());
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                lineNumbers.setText(getText());
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                lineNumbers.setText(getText());
            }

            private String getText(){
                int caretPosition = taBase.getDocument().getLength();
                Element root = taBase.getDocument().getDefaultRootElement();
                String text = "1"+System.getProperty("line.separator");
                for (int i = 0; i < root.getElementIndex(caretPosition); i++) {
                    if((i+1)%2 ==0)
                        painter = (new DefaultHighlighter.DefaultHighlightPainter(par));
                    else
                        painter = (new DefaultHighlighter.DefaultHighlightPainter(inpar));
                    try{
                    taBase.getHighlighter().addHighlight(taBase.getLineStartOffset(i), taBase.getLineEndOffset(i), painter);
                    }catch(BadLocationException ble)
                    {
                        ble.printStackTrace();
                    }
                    text+= (i+2) + System.getProperty("line.separator");
                }
                return text;
            }
        });
        jspKodigo.setRowHeaderView(lineNumbers);
    }
    
