    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        la = new LexicAnalyzer(taBase, this);
        la.stringAnalyzer();
        LinkedList lista1 = la.getTokenList();
        LinkedList lista2 = la.getErrorList();
        LinkedList lista3 = la.getVariableList();
        LinkedList lista4 = la.getContador();
        LinkedList lista5 = la.getAsignaciones();
        LinkedList lista6 = la.getContadorSemantica1();
        LinkedList lista7 = la.getLineaAsignaciones();
        LinkedList lista8 = la.getValoresSemantica();
        
        DefaultTableModel dtmTokens = (DefaultTableModel) tblTokens.getModel();
        dtmTokens.setRowCount(0);
        try{
            for (int i = 0; i < lista1.size(); i++){
                Token t = (Token) lista1.get(i);
                if(t.getNumero() != -2)
                    dtmTokens.addRow(((Token) lista1.get(i)).returnObject());
            }
        }catch(Exception e){}
        
        DefaultTableModel dtmErrors = (DefaultTableModel) tblErrors.getModel();
        dtmErrors.setRowCount(0);
        try{
            for (int i = 0; i < lista2.size(); i++)
                dtmErrors.addRow(((Errores)lista2.get(i)).returnObject());
        }catch(Exception e){
        }
        setNumbers();
        loadLineNumbers();
        setErrorLines();
        
        int returnVal = fc.showSaveDialog(this);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                File file = fc.getSelectedFile();
                Ut.generateExcel(file, lista1, lista2, la.getLineNumbers(), la.getCounters(), lista3, lista4, lista6, lista5, lista7, lista8);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

